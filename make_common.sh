set -e

rootDir=`pwd`/..
installDir=$MECHSRC_INSTALL

source $HOME/miniconda3/etc/profile.d/conda.sh

#===============================================================================
#===============================================================================
make_libidc()
{
	pushd $rootDir/libideepcolor/build

	ninja $1 -j `nproc`

	popd
}

#===============================================================================
# $1 = script name
# $2 = target
# $3 = directive
#===============================================================================
make_project()
{
	case "$2" in
		"libidc" ) make_libidc "$3" ;;
		"all" )
			make_libidc "$3"
			;;
		* ) 
			echo "$1: Unknown target '$2'"
			echo "./$1.sh [libidc|all]"
			exit 1
			;;
	esac
}

