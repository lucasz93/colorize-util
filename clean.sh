#!/bin/bash
set -e

#===============================================================================
#===============================================================================
rootDir=`pwd`/..
installDir=$MECHSRC_INSTALL

#===============================================================================
#===============================================================================
clean_libidc()
{
	rm -rf "$rootDir/libideepcolor/build"
}

#===============================================================================
#===============================================================================
for target in "$@"
	do
	case "$target" in
		"libidc" ) clean_libidc ;;
		"all" )
			clean_libidc
			
			rm -rf $installDir
			;;
		* ) 
			echo "clean: Unknown target '$build_type'"
			echo "./clean.sh [libidc|all]"
			exit 1
			;;
	esac
done
